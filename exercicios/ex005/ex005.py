# o programa desta vez e, ler um numero inteiro  e mostrar na tela o seu antecessor e seu sucessor 

# numero que o usuario vai digitar
numero = int(input('Digite um numero:'))

# somando e subitraindo os valores
antecessor = numero - 1 
sucessor = numero + 1 

# mostrando o resultado
print('Analizando o numero')
print(F'{numero} seu antecessor e {antecessor} e seu sucessor e {sucessor}')


